#   Nested for loop

colors = ["green","yellow","purple"]

fruits = ["apples","banana","berries"]

for x in colors:
    for y in fruits:
        print(x,y)  # green apples
                    # green banana
                    # green berries
                    # yellow apples
                    # yellow banana
                    # yellow berries
                    # purple apples
                    # purple banana
                    # purple berries

